package regle;

/**
 * Enumeration permettant de differencier les differents vainqueurs
 * @author Pierre CHEMIN
 * @version 1.0
 */
public enum Vainqueur {
    /**Si le vainqueur est le camp Blanc */
    blanc,
    /**Si le vainqueur est le camp Noir*/
    noir,
    /**S'il n'y a encore aucun vainqueur */
    aucun,
    /**En cas d'egalite */
    egalite
}
